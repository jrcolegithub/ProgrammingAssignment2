cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'x'
  m <- x$getInv()
  if(!is.null(m)) {
    message("getting cached data")
    return(m)
  }
  data <- x$get()
  m <- solve(data, ...)
  x$setInv(m)
  m
}
mat = matrix(c(1,2,3,4),nrow=2)
matCache <- makeCacheMatrix(mat)
cacheSolve(matCache)
cacheSolve(matCache)